// 필터: 진짜 3글자 한글인지 확인
function is3CharKorean(word) {
  const chars = [...word];
  return chars.length === 3 && chars.every(c => /[가-힣]/.test(c));
}

function makeRow(word, isPlaceName) {
  const chars = [...word];
  return {
    word,
    is_valid:       true,
    is_person_name: false,
    is_place_name:  isPlaceName,
    first_char:     chars[0],
    last_char:      chars[2],
    source:         'manual',
  };
}

async function upsertBatch(rows) {
  if (rows.length === 0) return 0;
  const { error } = await supabase
    .from('words')
    .upsert(rows, { onConflict: 'word' });
  if (error) {
    console.error('  upsert 오류:', error.message);
    return 0;
  }
  return rows.length;
}

async function main() {
  // 일반 명사 필터링
  const nounRows = NOUNS
    .filter(is3CharKorean)
    .map(w => makeRow(w, false));

  // 지명 필터링
  const placeRows = PLACES
    .filter(is3CharKorean)
    .map(w => makeRow(w, true));

  const allRows = [...nounRows, ...placeRows];

  // 중복 제거
  const unique = [...new Map(allRows.map(r => [r.word, r])).values()];

  console.log(`\n총 ${unique.length}개 단어를 삽입합니다.`);
  console.log(`  일반 명사: ${nounRows.length}개`);
  console.log(`  지명: ${placeRows.length}개\n`);

  // 50개씩 배치 삽입
  const BATCH = 50;
  let total = 0;
  for (let i = 0; i < unique.length; i += BATCH) {
    const batch = unique.slice(i, i + BATCH);
    const count = await upsertBatch(batch);
    total += count;
    process.stdout.write(`\r  진행: ${total}/${unique.length}개`);
  }

  console.log(`\n\n완료! ${total}개 삽입됨.`);
}

main().catch(console.error);
